    private void btn_MaHoa(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_MaHoa
        // TODO add your handling code here:
        try {
            String plain = jTextArea1.getText();
            String cipher = ecc.encrypt(plain);
            jTextArea4.setText(cipher);
        } catch (Exception e) {
            jTextArea4.setText("Lỗi mã hóa");
        }
    }//GEN-LAST:event_btn_MaHoa

    private void btn_GiaiMa(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GiaiMa
        // TODO add your handling code here:
         try {
            String cipher = jTextArea4.getText();
            String plain = ecc.decrypt(cipher);
            jTextArea1.setText(plain);
        } catch (Exception e) {
            jTextArea1.setText("Lỗi giải mã");
        }
    }//GEN-LAST:event_btn_GiaiMa

