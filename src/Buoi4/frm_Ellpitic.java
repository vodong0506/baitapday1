    private void btn_GenerateKeys(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GenerateKeys
        // TODO add your handling code here:
        try {
            ecc.generateKeys();
            jTextArea2.setText(ecc.getPublicKey());
            jTextArea3.setText(ecc.getPrivateKey());
        } catch (Exception e) {
            jTextArea2.setText("Lỗi tạo khóa");
        }
    }//GEN-LAST:event_btn_GenerateKeys

